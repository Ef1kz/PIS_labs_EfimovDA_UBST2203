def filter_students_by_average(students, average):
    """
    –§–£–ù–ö–¶–ò–Ø –î–õ–Ø –§–ò–õ–¨–¢–†–ê–¶–ò–ò –°–¢–£–î–ï–ù–¢–û–í –ü–û –°–†–ï–î–ù–ï–ú–£ –ë–ê–õ–õ–£
    students - —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –¥–ª—è —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏
    average - –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–π —Å—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª –¥–ª—è –æ—Ç–±–æ—Ä–∞
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç: —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ —Å —Å—Ä–µ–¥–Ω–∏–º –±–∞–ª–ª–æ–º >= average
    """
    filtered_students = []  # –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –¥–ª—è –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤

    # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –≤—Å–µ—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –≤ –∏—Å—Ö–æ–¥–Ω–æ–º —Å–ø–∏—Å–∫–µ
    for student in students:
        # –í–´–ß–ò–°–õ–Ø–ï–ú –°–†–ï–î–ù–ò–ô –ë–ê–õ–õ –°–¢–£–î–ï–ù–¢–ê:
        # sum(student["marks"]) - —Å—É–º–º–∏—Ä—É–µ—Ç –≤—Å–µ –æ—Ü–µ–Ω–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–∞
        # len(student["marks"]) - —Å—á–∏—Ç–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—Ü–µ–Ω–æ–∫
        # –î–µ–ª–∏–º —Å—É–º–º—É –Ω–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ - –ø–æ–ª—É—á–∞–µ–º —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ
        avg = sum(student["marks"]) / len(student["marks"])

        # –ü–†–û–í–ï–†–Ø–ï–ú, –ü–û–î–•–û–î–ò–¢ –õ–ò –°–¢–£–î–ï–ù–¢ –ü–û –°–†–ï–î–ù–ï–ú–£ –ë–ê–õ–õ–£:
        if avg >= average:
            # –ï—Å–ª–∏ —Å—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª >= –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è, –¥–æ–±–∞–≤–ª—è–µ–º —Å—Ç—É–¥–µ–Ω—Ç–∞ –≤ –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫
            filtered_students.append(student)

    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤
    return filtered_students

def calculate_average(marks):
    """
    –í–°–ü–û–ú–û–ì–ê–¢–ï–õ–¨–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø –î–õ–Ø –†–ê–°–ß–ï–¢–ê –°–†–ï–î–ù–ï–ì–û –ë–ê–õ–õ–ê
    marks - —Å–ø–∏—Å–æ–∫ –æ—Ü–µ–Ω–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–∞
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç: —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ –æ—Ü–µ–Ω–æ–∫
    """
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –æ—Ü–µ–Ω–∫–∏ —É —Å—Ç—É–¥–µ–Ω—Ç–∞ (—á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 0)
    if len(marks) == 0:
        return 0  # –ï—Å–ª–∏ –æ—Ü–µ–Ω–æ–∫ –Ω–µ—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º 0

    # –°—á–∏—Ç–∞–µ–º —Å—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª: —Å—É–º–º–∞ –æ—Ü–µ–Ω–æ–∫ / –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—Ü–µ–Ω–æ–∫
    return sum(marks) / len(marks)

